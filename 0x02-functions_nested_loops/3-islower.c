#include "holberton.h"
#include <ctype.h>

/**
 * _islower - Function that checks for lowercase
 * @c: is the int that will use for the argument of the function
 * Return: 1 if lowercase character 0 if not
 */
int _islower(int c)
{
	int k = _islower(c);

	if (k != 0)
		return (1);
	else
		return (0);
}
